import pandas as pd

# 定义分类处理规则函数
def 分类处理(df, 包含下限):
    分类结果 = {}
    分类范围 = [(0, 30), (30, 40), (40, 60), (60, 70), (70, 80), (80, 90), (90, 100)]

    for 下限, 上限 in 分类范围:
        if 下限 == 0 and 包含下限:  # 对于0-30范围，包含下限
            符合条件的数据 = df[(df['0的百分比'] >= 下限) & (df['0的百分比'] <= 上限)]
        else:
            符合条件的数据 = df[(df['0的百分比'] > 下限) & (df['0的百分比'] <= 上限)]
        分类名称 = f'{下限}-{上限}%'
        分类结果[分类名称] = 符合条件的数据

    return 分类结果

# 定义统计分类总数和百分比的函数
def 统计分类总数和百分比(分类结果, 统计数据):
    统计结果 = []
    for 分类名称, 数据 in 分类结果.items():
        总个数 = 数据['出现个数'].sum()
        总百分比 = 数据['百分比'].sum()
        统计结果.append({
            '分类': 分类名称,
            '总个数': 总个数,
            '总百分比': 总百分比
        })
    统计结果df = pd.DataFrame(统计结果)
    return 统计结果df

# 处理并保存每个工作表的数据
def 处理并保存数据(文件路径, 工作表名称, 年份):
    df = pd.read_excel(文件路径, sheet_name=工作表名称, dtype={'类别': str})

    for 长度 in [8, 9]:
        筛选数据 = df[df['类别'].str.len() == 长度]
        分类结果 = 分类处理(筛选数据, 包含下限=True)
        统计结果 = 统计分类总数和百分比(分类结果, 筛选数据)

        输出文件名 = f'{年份}庄家数串{长度}统计数据.xlsx'
        with pd.ExcelWriter(输出文件名) as writer:
            for 分类名称, 数据 in 分类结果.items():
                数据.to_excel(writer, sheet_name=分类名称, index=False)
            统计结果.to_excel(writer, sheet_name='分类总数和百分比统计', index=False)

# 文件路径和工作表名称列表
文件路径 = 'NewModel-澳门2021-2023庄家模型数据-独立年份240104.xlsx'
工作表名称列表 = ['新澳门2023_统计', '新澳门2022_统计', '新澳门2021_统计']
# 对每个工作表进行处理并保存数据
for 工作表名称 in 工作表名称列表:
    # 假设年份总是工作表名称中的前四个字符
    年份 = 工作表名称[3:7]
    处理并保存数据(文件路径, 工作表名称, 年份)



